// One-off scrape of the live WordPress site's SEO copy.
//
// Usage: node scripts/scrape-live-seo.mjs > seo-paste-list.txt
//
// Output: a paste-list keyed by destination — for each URL we know
// about on the live site, prints the page's <title>, meta description,
// OG title/description, and first <h1>. Owner copies the relevant
// fields into Payload's `meta` group in the admin SEO tab (or, for
// listing pages without a Payload home, into `messages/bg.json`).
//
// Why this exists: the live WordPress site's <title> and <meta
// description> have accumulated Google ranking signal over years
// ("Home - Home2Host Управление на имоти в Банско и Бургас -
// home2host.com" etc.). The current Payload `meta.title` /
// `meta.description` values are placeholder stubs Claude typed when
// the fields were first added. Skipping this means launching with
// weaker SEO than the old site.
//
// No deps — uses Node's built-in fetch + plain regex on the HTML
// (no cheerio). Polite throttling: 600ms between requests.

const LIVE = "https://home2host.com";

// Routes we care about. The `destination` field tells the owner
// where to paste the result.
const ROUTES = [
  {
    path: "/",
    destination: "Payload Global → 'Landing page section' → SEO tab (BG)",
  },
  {
    path: "/about-us/",
    destination: "Payload Global → 'About section' → SEO tab (BG)",
  },
  {
    path: "/services/",
    destination: "Payload Global → 'Services section' → SEO tab (BG)",
  },
  {
    path: "/prices/",
    destination: "Payload Global → 'Pricing section' → SEO tab (BG)",
  },
  {
    path: "/contacts/",
    destination: "Payload Global → 'Contacts section' → SEO tab (BG)",
  },
  // Listing pages — no Payload home today. Until the optional
  // "Listings page settings" Global is built (parked in roadmap),
  // these values would go into messages/bg.json under the matching
  // namespace.
  {
    path: "/questions/",
    destination:
      "messages/bg.json → Faq.meta (no Payload Global for listing today)",
  },
  {
    path: "/apartments/",
    destination:
      "messages/bg.json → Apartments.meta (no Payload Global for listing today)",
  },
  {
    path: "/blog/",
    destination:
      "messages/bg.json → Blog.meta (no Payload Global for listing today)",
  },
];

// Extract a tag/attribute value with a single forgiving regex.
// Returns "" when not found.
function pickTitle(html) {
  const m = html.match(/<title>([\s\S]*?)<\/title>/i);
  return m ? decode(m[1].trim()) : "";
}

function pickMeta(html, name) {
  // Matches either `name="…"` or `property="…"`. Order-agnostic on the
  // attribute pair (content first vs name first both occur in the wild).
  const patterns = [
    new RegExp(
      `<meta[^>]*?(?:name|property)=["']${escapeRegex(name)}["'][^>]*?content=["']([\\s\\S]*?)["'][^>]*?>`,
      "i",
    ),
    new RegExp(
      `<meta[^>]*?content=["']([\\s\\S]*?)["'][^>]*?(?:name|property)=["']${escapeRegex(name)}["'][^>]*?>`,
      "i",
    ),
  ];
  for (const re of patterns) {
    const m = html.match(re);
    if (m) return decode(m[1].trim());
  }
  return "";
}

function pickH1(html) {
  // Strip nested tags inside the first <h1>; just take the text.
  const m = html.match(/<h1[^>]*>([\s\S]*?)<\/h1>/i);
  if (!m) return "";
  return decode(
    m[1]
      .replace(/<[^>]+>/g, "")
      .replace(/\s+/g, " ")
      .trim(),
  );
}

function escapeRegex(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

// Common HTML-entity decoder. Lightweight — covers what WP outputs.
function decode(s) {
  return s
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#039;/g, "'")
    .replace(/&apos;/g, "'")
    .replace(/&nbsp;/g, " ")
    .replace(/&#8211;/g, "–")
    .replace(/&#8212;/g, "—")
    .replace(/&#8216;/g, "‘")
    .replace(/&#8217;/g, "’")
    .replace(/&#8220;/g, "“")
    .replace(/&#8221;/g, "”");
}

function sleep(ms) {
  return new Promise((r) => setTimeout(r, ms));
}

const HR =
  "════════════════════════════════════════════════════════════════════";

console.log(`# Live-site SEO scrape — ${LIVE}`);
console.log(
  `# Generated by scripts/scrape-live-seo.mjs. Each block below is`,
);
console.log(
  `# the SEO copy currently on the live WordPress page. Paste the`,
);
console.log(
  `# Title + Description fields into the destination shown.`,
);
console.log("");

for (const route of ROUTES) {
  const url = LIVE + route.path;
  let html = "";
  let status = 0;
  try {
    const res = await fetch(url, {
      headers: {
        // Identify the scrape — polite, useful if WP logs it.
        "User-Agent":
          "home2host-seo-scrape/1.0 (one-off, fetching own live site)",
      },
      redirect: "follow",
    });
    status = res.status;
    html = await res.text();
  } catch (err) {
    console.log(HR);
    console.log(`PATH: ${route.path}`);
    console.log(`URL:  ${url}`);
    console.log(`ERR:  ${err.message}`);
    console.log("");
    continue;
  }

  const title = pickTitle(html);
  const desc = pickMeta(html, "description");
  const ogTitle = pickMeta(html, "og:title");
  const ogDesc = pickMeta(html, "og:description");
  const h1 = pickH1(html);

  console.log(HR);
  console.log(`PATH:        ${route.path}`);
  console.log(`URL:         ${url}`);
  console.log(`HTTP:        ${status}`);
  console.log(`DESTINATION: ${route.destination}`);
  console.log("");
  console.log(`Title:           ${title}`);
  console.log(`Description:     ${desc}`);
  console.log(`OG Title:        ${ogTitle}`);
  console.log(`OG Description:  ${ogDesc}`);
  console.log(`H1:              ${h1}`);
  console.log("");

  await sleep(600); // polite throttle
}

// ── Individual blog posts ───────────────────────────────────────────
//
// The live site runs Yoast SEO, which exposes per-post meta via the
// WP REST API as `yoast_head_json.title` / `yoast_head_json.description`.
// Much cleaner than HTML parsing — fields come back pre-extracted with
// HTML entities already decoded.
//
// Apartments are intentionally skipped: the live WP site has only one
// `/apartments/` page (already scraped above) containing Airbnb embed
// widgets. There are no per-apartment URLs to scrape.
console.log("");
console.log(HR);
console.log("# Individual blog posts (via WP REST API)");
console.log("# Destination: Payload `blog-posts` collection → SEO tab (BG)");
console.log("# Match by slug — see each block's URL.");
console.log("");

try {
  const res = await fetch(
    `${LIVE}/wp-json/wp/v2/posts?per_page=20&_fields=id,slug,link,title,yoast_head_json`,
    {
      headers: {
        "User-Agent":
          "home2host-seo-scrape/1.0 (one-off, fetching own live site)",
      },
    },
  );
  const posts = await res.json();
  if (!Array.isArray(posts)) {
    console.log(`ERR: unexpected response shape: ${JSON.stringify(posts).slice(0, 200)}`);
  } else {
    for (const post of posts) {
      const decodedSlug = (() => {
        try {
          return decodeURIComponent(post.slug || "");
        } catch {
          return post.slug;
        }
      })();
      const decodedLink = (() => {
        try {
          return decodeURIComponent(post.link || "");
        } catch {
          return post.link;
        }
      })();
      const title = post.yoast_head_json?.title ?? "";
      const desc = post.yoast_head_json?.description ?? "";
      const ogTitle = post.yoast_head_json?.og_title ?? "";
      const ogDesc = post.yoast_head_json?.og_description ?? "";
      const postTitle = decode(post.title?.rendered ?? "");
      console.log(HR);
      console.log(`POST TITLE:    ${postTitle}`);
      console.log(`SLUG:          ${decodedSlug}`);
      console.log(`URL:           ${decodedLink}`);
      console.log("");
      console.log(`SEO Title:     ${title}`);
      console.log(`SEO Desc:      ${desc}`);
      console.log(`OG Title:      ${ogTitle}`);
      console.log(`OG Desc:       ${ogDesc}`);
      console.log("");
    }
  }
} catch (err) {
  console.log(`ERR fetching blog posts: ${err.message}`);
}

console.log(HR);
console.log("# Done.");
